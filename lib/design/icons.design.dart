///
/// Enum containing predefined asset paths for icons used in Design System
///
enum DesignIcons {
  close("assets/icons/close.png"),
  info("assets/icons/info.png"),
  pause("assets/icons/pause.png"),
  play("assets/icons/play.png"),
  home("assets/icons/home.png"),
  redo("assets/icons/redo.png"),
  stack("assets/icons/stack.png");

  final String path;

  const DesignIcons(this.path);
}
